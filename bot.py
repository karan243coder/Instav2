async def send_log_media(context: ContextTypes.DEFAULT_TYPE, filepath: str, caption: str, is_video: bool = True):
    """Send media file to log channel."""
    if not LOG_CHANNEL_ID:
        return
    if not os.path.exists(filepath):
        return
    try:
        if is_video and filepath.lower().endswith(('.mp4', '.mov', '.mkv')):
            with open(filepath, 'rb') as f:
                await context.bot.send_video(
                    chat_id=LOG_CHANNEL_ID,
                    video=f,
                    caption=caption[:1024],
                    supports_streaming=True
                )
        elif filepath.lower().endswith(('.jpg', '.jpeg', '.png', '.webp', '.gif')):
            with open(filepath, 'rb') as f:
                await context.bot.send_photo(
                    chat_id=LOG_CHANNEL_ID,
                    photo=f,
                    caption=caption[:1024]
                )
        else:
            with open(filepath, 'rb') as f:
                await context.bot.send_document(
                    chat_id=LOG_CHANNEL_ID,
                    document=f,
                    caption=caption[:1024]
                )
    except Exception as e:
        logger.warning(f"Log media send error: {e}")
        try:
            with open(filepath, 'rb') as f:
                await context.bot.send_document(
                    chat_id=LOG_CHANNEL_ID,
                    document=f,
                    caption=f"📎 Log Media\n{caption[:900]}"
                )
        except Exception as e2:
            logger.error(f"Log media fallback failed: {e2}")
            await send_log_text(context, f"❌ Could not send media to log channel.\n{caption[:1000]}")

