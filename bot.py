def instagram_headers() -> dict:
    return {
        "User-Agent": "Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/124.0.0.0 Safari/537.36",
        "Accept": "*/*",
        "X-IG-App-ID": "936619743392459",
        "X-Requested-With": "XMLHttpRequest",
        "Referer": "https://www.instagram.com/",
    }

def fetch_profile_info(username: str) -> Optional[dict]:
    """Try to fetch profile via web API. Returns None if blocked."""
    url = f"https://www.instagram.com/api/v1/users/web_profile_info/?username={username}"
    headers = instagram_headers()
    try:
        resp = requests.get(url, headers=headers, timeout=15, cookies={'sessionid': IG_SESSION_ID} if IG_SESSION_ID else None)
        if resp.status_code == 200:
            data = resp.json()
            return data.get("data", {}).get("user", {})
    except Exception as e:
        logger.warning(f"Profile fetch failed: {e}")
    return None

