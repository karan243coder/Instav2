# ==================== YT-DLP HELPERS ====================
def run_yt_dlp_info(url: str, cookiefile: Optional[str] = None) -> Tuple[Optional[dict], Optional[str]]:
    """Extract info without downloading."""
    opts = {
        'quiet': True,
        'no_warnings': True,
        'skip_download': True,
    }
    if cookiefile and os.path.exists(cookiefile):
        opts['cookiefile'] = cookiefile
    try:
        with yt_dlp.YoutubeDL(opts) as ydl:
            info = ydl.extract_info(url, download=False)
            return info, None
    except Exception as e:
        return None, str(e)

def run_yt_dlp_download(url: str, cookiefile: Optional[str] = None) -> Tuple[Optional[dict], Optional[str]]:
    """Download media, return info and filepath."""
    out_dir = tempfile.mkdtemp()
    opts = {
        'outtmpl': os.path.join(out_dir, '%(id)s.%(ext)s'),
        'quiet': True,
        'no_warnings': True,
        'format': 'best[filesize<50M]/best',
        'max_filesize': 50 * 1024 * 1024,
    }
    if cookiefile and os.path.exists(cookiefile):
        opts['cookiefile'] = cookiefile
    try:
        with yt_dlp.YoutubeDL(opts) as ydl:
            info = ydl.extract_info(url, download=True)
            filepath = ydl.prepare_filename(info)
            # yt-dlp sometimes changes extension (e.g., mp4 -> jpg for images)
            if not os.path.exists(filepath):
                base = os.path.splitext(filepath)[0]
                for f in os.listdir(out_dir):
                    if f.startswith(os.path.basename(base)):
                        filepath = os.path.join(out_dir, f)
                        break
            if os.path.exists(filepath) and os.path.getsize(filepath) > 0:
                return info, filepath
            return info, None
    except Exception as e:
        return None, str(e)

async def safe_send_media(update: Update, filepath: str, caption: str, is_video: bool = True):
    """Safely send media, fallback to document."""
    try:
        if is_video and filepath.lower().endswith(('.mp4', '.mov', '.mkv')):
            with open(filepath, 'rb') as f:
                await update.message.reply_video(video=f, caption=caption[:1024], supports_streaming=True)
        elif filepath.lower().endswith(('.jpg', '.jpeg', '.png', '.webp', '.gif')):
            with open(filepath, 'rb') as f:
                await update.message.reply_photo(photo=f, caption=caption[:1024])
        else:
            with open(filepath, 'rb') as f:
                await update.message.reply_document(document=f, caption=caption[:1024])
        return True
    except Exception as e:
        logger.error(f"Send media error: {e}")
        try:
            with open(filepath, 'rb') as f:
                await update.message.reply_document(document=f, caption=caption[:1024])
            return True
        except Exception as e2:
            logger.error(f"Document send error: {e2}")
            return False

